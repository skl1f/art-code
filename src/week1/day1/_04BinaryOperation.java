public class _04BinaryOperation {
    public static void main(String[] args) {
        // adding
        int age = 26;
        int age1 = 36;
        int sum = age + age1 + 15;

        // multiply
        int multi = age * age1;

        // dividing
        int num = 10;
        int div1 = num % 3;
        double div = num / 3;

        System.out.println(sum);
        System.out.println(multi);
        System.out.println(div1);
        System.out.println(div);
    }
}
